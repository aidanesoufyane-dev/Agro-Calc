  static Future<String> savePdfToTemp(

      Uint8List bytes, DateTime date) async {

    final dir = await getTemporaryDirectory();

    final d = date;

    final name =

      'AgroCalc_${d.year}'

      '${d.month.toString().padLeft(2,'0')}'

      '${d.day.toString().padLeft(2,'0')}_'

      '${d.hour.toString().padLeft(2,'0')}'

      '${d.minute.toString().padLeft(2,'0')}.pdf';

    final file = File('${dir.path}/$name');

    await file.writeAsBytes(bytes);

    return file.path;

  }



  static Future<void> sharePdf(

      Uint8List bytes, DateTime date) async {

    final path = await savePdfToTemp(bytes, date);

    await Share.shareXFiles(

      [XFile(path, mimeType: 'application/pdf')],

      subject: 'Rapport Fertigation NPK - AgroCalc Pro',

    );

  }



